package casting;

public class Upcasting {

	public static void main(String[] args) {
		
		//storing smaller data into larger  data type container -automatically
		int i=20;
		float f= 202.345f;
		double d=f;
		long l=i;
		byte c=23;
		short s=c;
		
		System.out.println(l);
		System.out.println(d);
		System.out.println(s);
		
		

	}

}
